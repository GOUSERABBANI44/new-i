#Input Values 
x = 10
y = 6

# Function for addition
def add(x, y):
    return x + y

# Function for Subtraction
def sub(x, y):
    return x - y

# Function for Multiplication
def mul(x, y):
    return x * y

# Function for Division
def div(x, y):
    return x / y

#Calling the function and printing the output

print(f"The value {x} + {y} = {add(x, y)}")
print(f"The value {x} - {y} = {sub(x, y)}")
print(f"The value {x} * {y} = {mul(x, y)}")
print(f"The value {x} / {y} = {div(x, y)}")