# Float Varibles
num1 = 12.5
num2 = 30.56

# Float Values Addition

Add = num1 + num2
print("Addition of two numbers:", Add)

# Float Values Subtraction

Sub = num1 - num2
print("Subtraction of two numbers:", Sub)

# Float Values Multiplication

Mul = num1 * num2
print("Multiplication of two numbers:", Mul)

# Float Values Division

Div = num1 / num2
print("Division of two numbers:", Div)


# Float Values Reminder

Rim = num1 % num2
print("Reminder of two numbers:", Rim)